package model;

/**
 *
 * @author Gumball
 */
public class MauGong {
    private String maMG;
    private String tenMG;
    private String moTaMG;

    public MauGong() {
    }

    public MauGong(String maMG, String tenMG, String moTaMG) {
        this.maMG = maMG;
        this.tenMG = tenMG;
        this.moTaMG = moTaMG;
    }

    public String getMaMG() {
        return maMG;
    }

    public void setMaMG(String maMG) {
        this.maMG = maMG;
    }

    public String getTenMG() {
        return tenMG;
    }

    public void setTenMG(String tenMG) {
        this.tenMG = tenMG;
    }

    public String getMoTaMG() {
        return moTaMG;
    }

    public void setMoTaMG(String moTaMG) {
        this.moTaMG = moTaMG;
    }
    
    
}
